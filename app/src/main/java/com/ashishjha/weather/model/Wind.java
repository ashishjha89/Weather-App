public class Wind {
    private int mChill;
    private int mSpeed;

    public void setChill(int c) {
        mChill = c;
    }

    public int getChill() {
        return mChill;
    }

    public void setSpeed(int s) {
        mSpeed = s;
    }

    public int getSpeed() {
        return mSpeed;
    }

}


